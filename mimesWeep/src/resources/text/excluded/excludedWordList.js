/**
 * Function to determine if an excluded word is a word in our username
 * Regex to detect a word is below, it will not flag if word characters are part of a larger word
 * But it will flag regardless of any surrounding puntuation, special characters, or numbers.
 * (?:^|[^a-zA-Z])shit(?:$|[^a-zA-Z])
 * @param {Name to check} username 
 * @returns True if word in username in excluded list, else False
 */
export function match(username) {

    // If an empty word return false
    if (username === undefined || username === null || username === "") {
        return false;
    }

    let lcUsername = username.toLowerCase();

    // We need to loop through every excluded word
    for (const excludedWord of excludedWords) {

        // Regex to match an excluded word in the username. 
        // It will find the word even if first word, or surrounded by special characters, punctuation, or numbers.
        // It will not find the word if it is part of a different larger word.
        let regEx = new RegExp("(?:^|[^a-zA-Z])" + excludedWord + "(?:$|[^a-zA-Z])");

        // If we find a match return true
        if (regEx.test(lcUsername)) {
            return true;
        }
    }

    // We did not find an excluded word
    return false;
}
